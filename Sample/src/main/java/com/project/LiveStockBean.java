package com.project;

public class LiveStockBean 
{
	private int itemid;
	private String itemBatchno;
	private int itemstock;
	private int itemSalePrice;
	private int itemCostPrice;
	private int itemMrp;
	
	
	
	
	public int getItemid() {
		return itemid;
	}
	public void setItemid(int itemid) {
		this.itemid = itemid;
	}
	public String getItemBatchno() {
		return itemBatchno;
	}
	public void setItemBatchno(String itemBatchno) {
		this.itemBatchno = itemBatchno;
	}
	public int getItemstock() {
		return itemstock;
	}
	public void setItemstock(int itemstock) {
		this.itemstock = itemstock;
	}
	public int getItemSalePrice() {
		return itemSalePrice;
	}
	public void setItemSalePrice(int itemSalePrice) {
		this.itemSalePrice = itemSalePrice;
	}
	public int getItemCostPrice() {
		return itemCostPrice;
	}
	public void setItemCostPrice(int itemCostPrice) {
		this.itemCostPrice = itemCostPrice;
	}
	public int getItemMrp() {
		return itemMrp;
	}
	public void setItemMrp(int itemMrp) {
		this.itemMrp = itemMrp;
	} 
	
	
	
	

}
